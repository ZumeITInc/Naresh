#string methods
string = 'python for everyone'

# string capatalize
string.capitalize()
print(string.capitalize())

# this function converts first character of each word in the given string into upper case
string.title()
print(string.title())

# Islower(): this function checks whether the given string contains all lower case letters or not.
# If all are lower case then it will return True else False.
string.islower()
print(string.islower())

# isupper():this function checks whether the given string contains all upper case letters or not.
# If all are upper case then it will return True else False.
string.isupper()
print(string.isupper())

# lower(): this function converts all letters of given string into lower case.
string.lower()
print(string.lower())

# upper():this function converts all letters of given string into upper case.
string.upper()
print(string.upper())

# len(): this function counts the number of characters in the given string.
len(string)
print(len(string))

# count() : this function counts no.of occurrences of a specific character in the given string.
string.count('o')
print(string.count('o'))

# find(): this function finds the index position of specific character in the given string.
string.find('p')
print(string.find('p'))

# split(): this function splits the given strings into multiple strings
string.split()
print(string.split())

# lstrip(): this function removes specific special character to left side of given string.
string.lstrip()
print(string.lstrip())

